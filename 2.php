  	   ->成分を加える("pepper", 成分の種類::カップ, 105)
  	   ->成分を加える("salt", 成分の種類::ドライ, 102)
  	   ->成分を加える("tortilla", 成分の種類::ドライ, 32)
  	   ->成分を加える("water", 成分の種類::湿った, 121)
  	   ->成分を加える("guacamole", 成分の種類::カップ, 116)
  	   ->成分を加える("extra virgin olive oil", 成分の種類::スプーン, 104)
  	   ->成分を加える("fajita veggies", 成分の種類::カップ, 103)
  	   ->成分を加える("carne asada", 成分の種類::ドライ, 105)
